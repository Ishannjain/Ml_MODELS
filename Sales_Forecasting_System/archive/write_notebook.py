        "# Aggregate sub-category metrics\n",
        "sub_yearly = df.groupby(['Sub-Category', 'Year'])['Sales'].sum().unstack(level=1).fillna(0)\n",
        "sub_growth_rate = ((sub_yearly[2018] - sub_yearly[2017]) / sub_yearly[2017]) * 100\n",
        "sub_volume = df.groupby('Sub-Category')['Sales'].sum()\n",
        "sub_monthly_sales = df.groupby(['Sub-Category', pd.Grouper(key='Order Date', freq='MS')])['Sales'].sum().unstack(level=0).fillna(0)\n",
