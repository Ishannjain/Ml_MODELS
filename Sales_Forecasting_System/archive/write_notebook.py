        "ts_weekly = df.resample('W', on='Order Date')['Sales'].sum()\n",
        "\n",
        "# 1. Isolation Forest\n",
        "iso_forest = IsolationForest(contamination=0.05, random_state=42)\n",
        "sales_features = ts_weekly.values.reshape(-1, 1)\n",
        "iso_anomalies = iso_forest.fit_predict(sales_features)\n",
        "anomaly_indices_if = np.where(iso_anomalies == -1)[0]\n",
        "\n",
        "# 2. Rolling Z-Score (8-week rolling window)\n",
        "rolling_mean = ts_weekly.rolling(window=8, min_periods=1).mean()\n",
        "rolling_std = ts_weekly.rolling(window=8, min_periods=1).std()\n",
        "rolling_std.iloc[0] = rolling_std.iloc[1] if len(rolling_std) > 1 else 1.0\n",
        "z_scores = (ts_weekly - rolling_mean) / rolling_std\n",
        "anomaly_indices_z = np.where(np.abs(z_scores) > 2)[0]\n",
        "\n",
        "# Plot anomalies\n",
        "fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(12, 10), sharex=True)\n",
