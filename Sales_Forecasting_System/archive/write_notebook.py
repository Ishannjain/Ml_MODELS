        "clustering_df = pd.DataFrame({\n",
        "    'Total Volume': sub_volume,\n",
        "    'YoY Growth': sub_growth_rate,\n",
        "    'Sales Volatility': sub_volatility,\n",
        "    'Avg Order Value': sub_avg_order\n",
        "}).fillna(0)\n",
        "\n",
        "# Scale features\n",
        "scaler = StandardScaler()\n",
        "scaled_features = scaler.fit_transform(clustering_df)\n",
        "\n",
        "# Elbow Method\n",
        "inertia = []\n",
        "for k in range(1, 10):\n",
        "    kmeans = KMeans(n_clusters=k, random_state=42)\n",
        "    kmeans.fit(scaled_features)\n",
        "    inertia.append(kmeans.inertia_)\n",
        "\n",
        "plt.figure(figsize=(10, 5))\n",
        "plt.plot(range(1, 10), inertia, marker='o', color='#2ca02c')\n",
        "plt.title('Elbow Method for Product Demand Clustering', fontsize=14, fontweight='bold')\n",
        "plt.xlabel('Number of Clusters (K)')\n",
        "plt.ylabel('Inertia')\n",
        "plt.tight_layout()\n",
        "plt.show()\n",
        "\n",
        "# K-Means on optimal K=4 clusters\n",
        "kmeans = KMeans(n_clusters=4, random_state=42)\n",
        "clustering_df['Cluster'] = kmeans.fit_predict(scaled_features)\n",
        "\n",
        "# PCA for 2D visualization\n",
        "pca = PCA(n_components=2, random_state=42)\n",
        "pca_features = pca.fit_transform(scaled_features)\n",
        "clustering_df['PCA1'] = pca_features[:, 0]\n",
        "clustering_df['PCA2'] = pca_features[:, 1]\n",
        "\n",
        "# Unique Labeling\n",
        "final_labels = {\n",
        "    0: 'Low Volume, High Volatility',\n",
        "    1: 'Declining Demand',\n",
        "    2: 'Growing Demand',\n",
