        "    df_features['Month'] = df_features.index.month\n",
        "    df_features['Quarter'] = df_features.index.quarter\n",
        "    df_features['Season_Num'] = df_features['Month'].apply(lambda x: 1 if x in [12, 1, 2] else (2 if x in [3, 4, 5] else (3 if x in [6, 7, 8] else 4)))\n",
